using Content.Shared.AU14.WithdrawConsole;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.AU14.WithdrawConsole;

[GenerateTypedNameReferences]
public sealed partial class WithdrawConsoleWindow : DefaultWindow
{
    public WithdrawConsoleWindow()
    {
        RobustXamlLoader.Load(this);
    }

    public void UpdateState(WithdrawConsoleBuiState state)
    {
        if (state.RoundEndTriggered)
        {
            StatusLabel.Text = Loc.GetString("withdraw-console-ui-round-ended");
            ToggleWithdrawButton.Disabled = true;
            CancelButton.Disabled = true;
            StalemateButton.Disabled = true;
            TimerLabel.Text = string.Empty;
            return;
        }

        // ID swipe status
        SwipeCountLabel.Text = state.IsUnlocked
            ? Loc.GetString("withdraw-console-ui-unlocked")
            : Loc.GetString("withdraw-console-ui-swipe-count", ("count", state.SwipedIdCount));

        // Timer display
        if (state.WithdrawActive && state.SecondsRemaining.HasValue)
        {
            var remaining = TimeSpan.FromSeconds(state.SecondsRemaining.Value);
            TimerLabel.Text = Loc.GetString("withdraw-console-ui-timer",
                ("minutes", (int)remaining.TotalMinutes),
                ("seconds", remaining.Seconds));
            StatusLabel.Text = Loc.GetString("withdraw-console-ui-status-active");
        }
        else
        {
            TimerLabel.Text = string.Empty;
            StatusLabel.Text = state.IsUnlocked
                ? Loc.GetString("withdraw-console-ui-status-ready")
                : Loc.GetString("withdraw-console-ui-status-locked");
        }

        // Button states
        ToggleWithdrawButton.Disabled = !state.IsUnlocked || state.WithdrawActive;
        CancelButton.Disabled = !state.WithdrawActive || !state.CanCancel;
        CancelButton.Visible = state.WithdrawActive;

        StalemateButton.Disabled = !state.IsUnlocked;
        StalemateButton.Text = state.StalemateToggled
            ? Loc.GetString("withdraw-console-ui-stalemate-cancel")
            : Loc.GetString("withdraw-console-ui-stalemate-toggle");
    }
}
